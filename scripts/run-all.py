def load_metadata(root: Path) -> dict[str, Any]:
    path = metadata_path(root)
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except json.JSONDecodeError:
        return {}


def write_metadata(root: Path, metadata: dict[str, Any]) -> None:
    metadata_path(root).write_text(json.dumps(metadata, indent=2) + "\n", encoding="utf-8")


def pid_alive(pid: int | None) -> bool:
    if not pid:
        return False
    if is_windows():
        completed = subprocess.run(
            ["tasklist", "/FI", f"PID eq {pid}"],
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            text=True,
        )
        return str(pid) in completed.stdout
    try:
        os.kill(pid, 0)
        return True
    except OSError:
        return False


def stop_pid(pid: int | None, name: str) -> None:
    if not pid or not pid_alive(pid):
        print(f"[ok] {name} is not running")
        return
    print(f"[info] Stopping {name} pid {pid}")
    if is_windows():
        subprocess.run(["taskkill", "/PID", str(pid), "/T", "/F"], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
    else:
        try:
            os.killpg(pid, signal.SIGTERM)
        except OSError:
            os.kill(pid, signal.SIGTERM)
    deadline = time.time() + 10
    while time.time() < deadline:
        if not pid_alive(pid):
            print(f"[ok] Stopped {name}")
            return
        time.sleep(0.25)
    if not is_windows():
        try:
            os.killpg(pid, signal.SIGKILL)
        except OSError:
            pass


def start_process(command: list[str], *, cwd: Path, env: dict[str, str] | None, log_path: Path) -> subprocess.Popen[Any]:
    command = resolve_command(command)
    log = log_path.open("ab")
    kwargs: dict[str, Any] = {
        "cwd": cwd,
        "env": env,
        "stdout": log,
        "stderr": subprocess.STDOUT,
        "stdin": subprocess.DEVNULL,
    }
    if is_windows():
        kwargs["creationflags"] = subprocess.CREATE_NEW_PROCESS_GROUP
    else:
        kwargs["start_new_session"] = True
    return subprocess.Popen(command, **kwargs)


def start_desktop_processes(root: Path, python_exe: Path) -> dict[str, Any]:
    existing = load_metadata(root)
    backend_pid = existing.get("desktop_backend_pid")
    frontend_pid = existing.get("desktop_frontend_pid")
    if pid_alive(backend_pid) and pid_alive(frontend_pid):
        print("[ok] Runner-managed desktop processes are already running")
        return existing

    stop_pid(backend_pid, "desktop-backend")
    stop_pid(frontend_pid, "desktop-frontend")

    logs = runtime_dir(root)
    backend = root / "apps" / "desktop-backend"
    frontend = root / "apps" / "desktop-frontend"
    backend_process = start_process(
        [str(python_exe), "-m", "uvicorn", "main:app", "--host", "127.0.0.1", "--port", "8001"],
        cwd=backend,
        env=desktop_backend_env(root),
        log_path=logs / "desktop-backend.log",
    )
    frontend_process = start_process(
        ["npm", "run", "dev", "--", "--host", "127.0.0.1", "--port", "1420"],
        cwd=frontend,
        env=os.environ.copy(),
        log_path=logs / "desktop-frontend.log",
    )
    time.sleep(2)
    failed = []
    if backend_process.poll() is not None:
        failed.append(("desktop-backend", backend_process.returncode, logs / "desktop-backend.log"))
    if frontend_process.poll() is not None:
        failed.append(("desktop-frontend", frontend_process.returncode, logs / "desktop-frontend.log"))
    if failed:
        for process in (backend_process, frontend_process):
            if process.poll() is None:
                stop_pid(process.pid, "partially-started desktop process")
        details = "; ".join(
            f"{name} exited with {code}. Last log lines: {tail_log(log_path)}"
            for name, code, log_path in failed
        )
        raise RunnerError(details)
    metadata = {
        "desktop_backend_pid": backend_process.pid,
        "desktop_frontend_pid": frontend_process.pid,
        "started_at": datetime.now(timezone.utc).isoformat(),
        "urls": {
            "desktop_backend": DESKTOP_BACKEND_URL,
            "desktop_frontend": DESKTOP_FRONTEND_URL,
        },
    }
    write_metadata(root, metadata)
    print(f"[ok] Started desktop-backend pid {backend_process.pid}")
    print(f"[ok] Started desktop-frontend pid {frontend_process.pid}")
    return metadata


def tail_log(path: Path, lines: int = 12) -> str:
    if not path.exists():
        return "<log missing>"
    content = path.read_text(encoding="utf-8", errors="replace").splitlines()
    return " | ".join(content[-lines:]) or "<log empty>"


def http_get(url: str, timeout: int = 10) -> tuple[int, bytes]:
    with request.urlopen(url, timeout=timeout) as response:
        return response.status, response.read()


def http_json(url: str, timeout: int = 10) -> Any:
    status, body = http_get(url, timeout)
    if status < 200 or status >= 400:
        raise RunnerError(f"{url} returned HTTP {status}")
    return json.loads(body.decode("utf-8"))


def wait_for(name: str, url: str, *, json_expected: bool, timeout_seconds: int = 120) -> None:
    print(f"[wait] {name} {url}")
    deadline = time.time() + timeout_seconds
    last_error: Exception | None = None
    while time.time() < deadline:
        try:
            if json_expected:
                http_json(url, timeout=5)
            else:
                status, _ = http_get(url, timeout=5)
                if status < 200 or status >= 400:
                    raise RunnerError(f"HTTP {status}")
            print(f"[ok] {name} ready")
            return
        except Exception as exc:  # noqa: BLE001 - keep retrying transient startup failures.
            last_error = exc
            time.sleep(2)
    raise RunnerError(f"Timed out waiting for {name}: {last_error}")


def wait_for_all() -> None:
    wait_for("webapp-backend", f"{CENTRAL_BACKEND_URL}/health", json_expected=True)
    wait_for("file-server", f"{FILE_SERVER_URL}/health", json_expected=True)
    wait_for("webapp-frontend", CENTRAL_FRONTEND_URL, json_expected=False)
    wait_for("desktop-backend", f"{DESKTOP_BACKEND_URL}/health", json_expected=True)
    wait_for("desktop-frontend", DESKTOP_FRONTEND_URL, json_expected=False)


def seed_admin(root: Path) -> None:
    if is_windows() and (root / "scripts" / "seed-admin.ps1").exists():
        run(["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass", "-File", str(root / "scripts" / "seed-admin.ps1"), "-EnvFile", str(root / ".env")], cwd=root)
        return
    run(compose_command(root, "exec", "-T", "webapp-backend", "python", "scripts/seed.py", include_dev=True), cwd=root)


def command_up(root: Path, install_mode: str) -> None:
    ensure_env(root)
    run(compose_command(root, "up", "-d", *DOCKER_SERVICES, include_dev=True), cwd=root)
    wait_for("webapp-backend", f"{CENTRAL_BACKEND_URL}/health", json_expected=True)
    seed_admin(root)
    python_exe = ensure_desktop_backend(root, install_mode)
    ensure_desktop_frontend(root, install_mode)
    start_desktop_processes(root, python_exe)
    wait_for_all()
    print_urls_and_credentials(root)


def command_down(root: Path) -> None:
    metadata = load_metadata(root)
    stop_pid(metadata.get("desktop_frontend_pid"), "desktop-frontend")
    stop_pid(metadata.get("desktop_backend_pid"), "desktop-backend")
    if metadata_path(root).exists():
        metadata_path(root).unlink()
    if is_windows() and (root / "scripts" / "dev-down.ps1").exists():
        run(["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass", "-File", str(root / "scripts" / "dev-down.ps1"), "-EnvFile", str(root / ".env")], cwd=root)
    else:
        ensure_env(root)
        run(compose_command(root, "down", include_dev=True), cwd=root)


def command_health(root: Path) -> None:
    if is_windows() and (root / "scripts" / "dev-health.ps1").exists():
        run(["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass", "-File", str(root / "scripts" / "dev-health.ps1")], cwd=root)
        return
    checks = [
        ("webapp-backend health", f"{CENTRAL_BACKEND_URL}/health", True),
        ("file-server health", f"{FILE_SERVER_URL}/health", True),
        ("webapp-frontend", CENTRAL_FRONTEND_URL, False),
        ("desktop-backend health", f"{DESKTOP_BACKEND_URL}/health", True),
        ("desktop-frontend", DESKTOP_FRONTEND_URL, False),
    ]
    for name, url, wants_json in checks:
        if wants_json:
            http_json(url)
        else:
            status, _ = http_get(url)
            if status < 200 or status >= 400:
                raise RunnerError(f"{name} returned HTTP {status}")
        print(f"[ok] {name} {url}")
    print("Health checks completed.")


def command_smoke(root: Path) -> None:
    if is_windows() and (root / "scripts" / "e2e-smoke.ps1").exists():
        run(["powershell", "-NoProfile", "-ExecutionPolicy", "Bypass", "-File", str(root / "scripts" / "e2e-smoke.ps1")], cwd=root)
        return
    command_health(root)
    env_values = read_env_file(root / ".env")
    email = env_values.get("ADMIN_EMAIL")
    password = env_values.get("ADMIN_PASSWORD")
    if not email or not password:
        raise RunnerError("ADMIN_EMAIL and ADMIN_PASSWORD must be set in .env for login smoke.")
    login_payload = json.dumps(
        {"email": email, "password": password, "device_fingerprint": "rsap-runner-smoke-test"}
    ).encode("utf-8")
    login_request = request.Request(
        f"{CENTRAL_BACKEND_URL}/api/v1/auth/login",
        data=login_payload,
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    with request.urlopen(login_request, timeout=15) as response:
        login = json.loads(response.read().decode("utf-8"))
    data = login.get("data") or {}
    access_token = data.get("access_token")
    session_token = data.get("session_token")
    if not access_token or not session_token:
        raise RunnerError("Login response did not contain both access_token and session_token")
    print("[pass] admin login returned JWT and X-Session-Token")
    me_request = request.Request(
        f"{CENTRAL_BACKEND_URL}/api/v1/auth/me",
        headers={"Authorization": f"Bearer {access_token}", "X-Session-Token": session_token},
        method="GET",
    )
    with request.urlopen(me_request, timeout=10):
        pass
    print("[pass] protected central request accepted dual-token headers")
    print("End-user smoke checks completed.")


def command_status(root: Path) -> None:
    print("Git")
    run_capture(["git", "branch", "--show-current"], cwd=root)
    run_capture(["git", "rev-parse", "HEAD"], cwd=root)
    print("\nDocker Compose")
    ensure_env(root)
    run_capture(compose_command(root, "ps", include_dev=True), cwd=root, check=False)
    metadata = load_metadata(root)
    print("\nRunner-managed desktop processes")
    backend_pid = metadata.get("desktop_backend_pid")
    frontend_pid = metadata.get("desktop_frontend_pid")
    print(f"  desktop-backend pid={backend_pid} alive={pid_alive(backend_pid)}")
    print(f"  desktop-frontend pid={frontend_pid} alive={pid_alive(frontend_pid)}")
    print_urls()


