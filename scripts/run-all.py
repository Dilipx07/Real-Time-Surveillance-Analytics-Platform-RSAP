def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run the complete RSAP local development runtime.")
    parser.add_argument("command", choices=["up", "down", "health", "smoke", "restart", "status"])
    install = parser.add_mutually_exclusive_group()
    install.add_argument("--install", action="store_true", help="Install desktop dependencies even if present.")
    install.add_argument("--no-install", action="store_true", help="Do not install missing desktop dependencies.")
    return parser.parse_args(argv)


def install_mode(args: argparse.Namespace) -> str:
    if args.install:
        return "always"
    if args.no_install:
        return "never"
    return "missing"


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv or sys.argv[1:])
    try:
        root = find_repo_root()
        os.chdir(root)
        if args.command == "up":
            command_up(root, install_mode(args))
        elif args.command == "down":
            command_down(root)
        elif args.command == "health":
            command_health(root)
        elif args.command == "smoke":
            command_smoke(root)
        elif args.command == "restart":
            command_down(root)
            command_up(root, install_mode(args))
        elif args.command == "status":
            command_status(root)
        return 0
    except (RunnerError, error.URLError, TimeoutError) as exc:
        print(f"[error] {exc}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
